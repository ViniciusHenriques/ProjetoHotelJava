    private void preencherTabela(){
        try {
             QuartoServicos FS = ServicosFactory.getQuartoServicos();
             ArrayList<Quarto> func = FS.buscarQuarto();
             
             for(int i=0; i<func.size(); i++){
                dtm.addRow(new String[]{
                    String.valueOf(func.get(i).getIdQuarto()),
                    String.valueOf(func.get(i).getNum()),
                    String.valueOf(func.get(i).getValor()),
                    
                    func.get(i).getTipo(),
                    func.get(i).getAndar(),
                    func.get(i).getStatus(),
                    
                    String.valueOf(func.get(i).getNumComodos())});
                
             }//fecha for
             jTableQuarto.setModel(dtm);
         } catch (Exception e) {
             JOptionPane.showMessageDialog(rootPane,"Erro!"+e.getMessage());
         }//fecha catch
     }//fecha preenchertabela
    
    
