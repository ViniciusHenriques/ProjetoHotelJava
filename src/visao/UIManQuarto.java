     private void deletar(){
         try {
             int linha = jTableQuarto.getSelectedRow();
             if(linha ==-1){
                 JOptionPane.showMessageDialog(rootPane, "Selecione uma linha ");
             }else{
                 String id = jTableQuarto.getValueAt(linha, 0).toString();
                 int op = JOptionPane.showConfirmDialog(rootPane, "Você tem certeza que deseja excluir??","Alerta",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE );
                 if(op==0){
                     QuartoServicos ws = ServicosFactory.getQuartoServicos();
                     ws.deletar(Long.parseLong(id));
                             
                 }
             
             }
         } catch (Exception e) {
             JOptionPane.showMessageDialog(rootPane, "Erro "+e.getMessage());
         }
     }
     private void filtrar(){
         try {
             if(jtPesq.getText().isEmpty()){
                 limpar();
                 preencherTabela();
             }else{
                 String query = "";
                 String filtro = jComboPesq.getSelectedItem().toString();
                 if(filtro.equalsIgnoreCase("Código")){
                     query = "where idquarto like '"+jtPesq.getText()+"%'";
                 }else if(filtro.equalsIgnoreCase("N° Quarto")){
                     query = "where num like '%"+jtPesq.getText()+"%'";
                 
                 }else if(filtro.equalsIgnoreCase("Valor")){
                     query = "where valor like '"+jtPesq.getText()+"%'";
                 }else if(filtro.equalsIgnoreCase("tipo")){
                     query = "where tipo like '"+jtPesq.getText()+"%'";
                 }else if(filtro.equalsIgnoreCase("andar")){
                     query = "where andar like '"+jtPesq.getText()+"%'";
                 }else if(filtro.equalsIgnoreCase("status")){
                     query = "where status like '"+jtPesq.getText()+"%'";
                 }else if(filtro.equalsIgnoreCase("N° Cômodos")){
                     query = "where numcomodos like '"+jtPesq.getText()+"%'";
                 }
                 QuartoServicos ps = ServicosFactory.getQuartoServicos();
                 ArrayList<Quarto> fun = ps.filtrar(query);
                 
                 for(int i = 0; i< fun.size();i++){
                    dtm.addRow(new String[]{
                        String.valueOf(fun.get(i).getIdQuarto()),
                        String.valueOf(fun.get(i).getNum()),
                        String.valueOf(fun.get(i).getValor()),
                        fun.get(i).getTipo(),
                        fun.get(i).getAndar(),
                        fun.get(i).getStatus(),
                                               
                        String.valueOf(fun.get(i).getNumComodos())
                    });
                }
                jTableQuarto.setModel(dtm);
             }
         } catch (Exception e) {
             jtPesq.setText(null);
            JOptionPane.showMessageDialog(rootPane, "Erro! "+e.getMessage());
         }
     }
     
     private void alterar(){
        
        int linha = jTableQuarto.getSelectedRow();
        
        if(linha == -1){
            JOptionPane.showMessageDialog(rootPane, "Selecione uma linha");
            return;
        }
        jtIdQuarto.setText(jTableQuarto.getValueAt(linha, 0).toString());
        jtNum.setText(jTableQuarto.getValueAt(linha, 1).toString());
        jtValor.setText(jTableQuarto.getValueAt(linha, 2).toString());
        
        if(jTableQuarto.getValueAt(linha, 3).toString().equalsIgnoreCase("luxo")){
            jrLuxo.setSelected(true);
        }else  if(jTableQuarto.getValueAt(linha, 3).toString().equalsIgnoreCase("cobertura")){
            jrCobertura.setSelected(true);
        }else if(jTableQuarto.getValueAt(linha, 3).toString().equalsIgnoreCase("casal")){
            jrCasal.setSelected(true);
        }else{
            jrSolteiro.setSelected(true);
        } 
         
         
        jtAndar.setText(jTableQuarto.getValueAt(linha, 4).toString());   
        
        
        
              
        jtNumComo.setText(jTableQuarto.getValueAt(linha, 6).toString());        
        
        
         
        
               
    }//fecha alterar
     
     private void confirmarAlteracao(){
        try {
            Quarto f = new Quarto();
            
            if(!util.validacao.validarNumero(jtNum.getText())){
                throw new NumberFormatException("Número invalido!");
            }
            if(!util.validacao.validarNumero(jtValor.getText())){
                throw new NumberFormatException("Valor invalido!");
            }
            if(!util.validacao.validarNumero(jtAndar.getText())){
                throw new NumberFormatException("Andar invalido!");
            }
            if(!util.validacao.validarNumero(jtNumComo.getText())){
                throw new NumberFormatException("Número de comodos invalido!");
            }
            f.setIdQuarto(Long.parseLong(jtIdQuarto.getText()));
            
            f.setNum(Integer.parseInt(jtNum.getText()));
            f.setValor(Double.parseDouble(jtValor.getText()));
            
            f.setAndar(jtAndar.getText());
            
            
            
            
            f.setNumComodos(Integer.parseInt(jtNumComo.getText()));
            
            int linha = jTableQuarto.getSelectedRow();
            f.setStatus((String)jTableQuarto.getValueAt(linha, 5));
            
            if(jrLuxo.isSelected()){
                f.setTipo(jrLuxo.getText());
            }else if(jrCobertura.isSelected()){
                f.setTipo(jrCobertura.getText());
            }else if(jrCasal.isSelected()){
                f.setTipo(jrCasal.getText());
            }else{
                f.setTipo(jrSolteiro.getText());
            }
            
            
            //Enviando o objeto pVO para o banco de dados
            QuartoServicos ps = ServicosFactory.getQuartoServicos();
            //Chamando o método 
            ps.alterarQuarto(f);
            
            JOptionPane.showMessageDialog(
                    rootPane, 
                    "Quarto alterado com sucesso!");
            
            limparCampos();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, 
                    "Erro! "+e.getMessage());
        }//fecha catch
    }//fecha confirmarAlteracao
