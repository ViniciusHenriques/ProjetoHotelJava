     private void deletar(){
         try {
             int linha = jTableFun.getSelectedRow();
             if(linha ==-1){
                 JOptionPane.showMessageDialog(rootPane, "Selecione uma linha ");
             }else{
                 String id = jTableFun.getValueAt(linha, 0).toString();
                 int op = JOptionPane.showConfirmDialog(rootPane, "Você tem certeza que deseja excluir??","Alerta",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE );
                 if(op==0){
                     FuncionarioServicos ws = ServicosFactory.getFuncionarioServicos();
                     ws.deletar(Long.parseLong(id));
                             
                 }
             
             }
         } catch (Exception e) {
             JOptionPane.showMessageDialog(rootPane, "Erro "+e.getMessage());
         }
     }
     private void filtrar(){
         try {
             if(jtPesq.getText().isEmpty()){
                 limpar();
                 preencherTabela();
             }else{
                 String query = "";
                 String filtro = jcomboPesq.getSelectedItem().toString();
                 if(filtro.equalsIgnoreCase("Código")){
                     query = "where idfuncionario like '"+jtPesq.getText()+"%'";
                 }else if(filtro.equalsIgnoreCase("Nome")){
                     query = "where nome like '%"+jtPesq.getText()+"%'";
                 
                 }else if(filtro.equalsIgnoreCase("Telefone")){
                     query = "where telefone like '"+jtPesq.getText()+"%'";
                 }else if(filtro.equalsIgnoreCase("cpf")){
                     query = "where cpf like '"+jtPesq.getText()+"%'";
                 }else if(filtro.equalsIgnoreCase("cargo")){
                     query = "where cargo like '"+jtPesq.getText()+"%'";
                 }else if(filtro.equalsIgnoreCase("sexo")){
                     query = "where sexo like '"+jtPesq.getText()+"%'";
                 }else if(filtro.equalsIgnoreCase("salario")){
                     query = "where salario like '"+jtPesq.getText()+"%'";
                 }
                 FuncionarioServicos ps = ServicosFactory.getFuncionarioServicos();
                 ArrayList<Funcionario> fun = ps.filtrar(query);
                 
                 for(int i = 0; i< fun.size();i++){
                    dtm.addRow(new String[]{
                        String.valueOf(fun.get(i).getIdFuncionario()),
                        fun.get(i).getNome(),
                        fun.get(i).getTelefone(),
                        fun.get(i).getCpf(),
                        fun.get(i).getCargo(),
                        fun.get(i).getSexo(),                       
                        String.valueOf(fun.get(i).getSalario())
                    });
                }
                jTableFun.setModel(dtm);
             }
         } catch (Exception e) {
             jtPesq.setText(null);
            JOptionPane.showMessageDialog(rootPane, "Erro! "+e.getMessage());
         }
     }
     
     private void alterar(){
        
        int linha = jTableFun.getSelectedRow();
        
        if(linha == -1){
            JOptionPane.showMessageDialog(rootPane, "Selecione uma linha");
            return;
        }
        
        
        
        jrCod.setText(jTableFun.getValueAt(linha, 0).toString());
        jtNome.setText(jTableFun.getValueAt(linha, 1).toString());
        jtSenha.setText(jTableFun.getValueAt(linha, 2).toString());
        jtTelefone.setText(jTableFun.getValueAt(linha, 3).toString());
        jtCPF.setText(jTableFun.getValueAt(linha, 4).toString());        
        if(jTableFun.getValueAt(linha, 5).toString().equalsIgnoreCase("gerente")){
            jrGere.setSelected(true);
        }else  if(jTableFun.getValueAt(linha, 5).toString().equalsIgnoreCase("recepcionista")){
            jrRecep.setSelected(true);
        }else if(jTableFun.getValueAt(linha, 5).toString().equalsIgnoreCase("secretária")){
            jrSecre.setSelected(true);
        }else  if(jTableFun.getValueAt(linha, 5).toString().equalsIgnoreCase("camareiro")){
            jrCam.setSelected(true);
        }else if(jTableFun.getValueAt(linha, 5).toString().equalsIgnoreCase("supervisor")){
            jrSuper.setSelected(true);
        }else if(jTableFun.getValueAt(linha, 5).toString().equalsIgnoreCase("controler")){
            jrCont.setSelected(true);
        }else  if(jTableFun.getValueAt(linha, 5).toString().equalsIgnoreCase("Cooder. de sistemas")){
            jrCoo.setSelected(true);
        }else{
            jrTel.setVisible(true);
        }   
        
        if(jTableFun.getValueAt(linha, 6).toString().equalsIgnoreCase("masculino")){
            jrMasculino.setSelected(true);
        } else {
            jrFeminino.setSelected(true);
        }
              
        jtSalario.setText(jTableFun.getValueAt(linha, 7).toString());        
        
        
         
        
               
    }//fecha alterar
     
     private void confirmarAlteracao(){
        try {
            Funcionario f = new Funcionario();
            
            if(!util.validacao.validarString(jtNome.getText())){

                throw new NumberFormatException("Nome invalido!");
            }
            
            if(jtSenha.getText().isEmpty()){
                throw new NumberFormatException("Senha invalida!");
            }
            if(!util.validacao.validarNumero(jtTelefone.getText())){
                throw new NumberFormatException("Telefone invalida!");
            }
            if(!util.validacao.validarCPF(jtCPF.getText())){
                throw new NumberFormatException("CPF invalido!");
            }
            if(!util.validacao.validarSalario(jtSalario.getText())){
                throw new NumberFormatException("Salário invalido!");
            }
            f.setIdFuncionario(Long.parseLong(jrCod.getText()));
            f.setNome(jtNome.getText());
            f.setSenha(jtSenha.getText());
            f.setTelefone(jtTelefone.getText());
            f.setCpf(jtCPF.getText());
            
            
            f.setSalario(Double.parseDouble(jtSalario.getText()));
            
            if(jrMasculino.isSelected()){
                f.setSexo(jrMasculino.getText());
            }else if(jrFeminino.isSelected()){
                f.setSexo(jrFeminino.getText());
            }else{
               
            }
            
            if(jrGere.isSelected()){
                f.setCargo(jrGere.getText());
            }else if(jrRecep.isSelected()){
                f.setCargo(jrRecep.getText());
            }else if(jrSecre.isSelected()){
                f.setCargo(jrSecre.getText());
            }else if(jrCam.isSelected()){
                f.setCargo(jrCam.getText());
            }else if(jrSuper.isSelected()){
                f.setCargo(jrSuper.getText());
            }else if(jrCont.isSelected()){
                f.setCargo(jrCont.getText());
            }else if(jrCoo.isSelected()){
                f.setCargo(jrCoo.getText());
            }else if(jrTel.isSelected()){
                f.setCargo(jrTel.getText());
            }else{
                throw new NumberFormatException("Selecione o Cargo!"); 
            }
            
            
            //Enviando o objeto pVO para o banco de dados
            FuncionarioServicos ps = ServicosFactory.getFuncionarioServicos();
            //Chamando o método 
            ps.alterarFuncionario(f);
            
            JOptionPane.showMessageDialog(
                    rootPane, 
                    "Funcionario alterado com sucesso!");
            limparCampos();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, 
                    "Erro! "+e.getMessage());
        }//fecha catch
    }//fecha confirmarAlteracao
