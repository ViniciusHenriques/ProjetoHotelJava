    private void preencherTabela(){
        try {
             FuncionarioServicos FS = ServicosFactory.getFuncionarioServicos();
             ArrayList<Funcionario> func = FS.buscarFuncionario();
             
             for(int i=0; i<func.size(); i++){
                dtm.addRow(new String[]{
                    String.valueOf(func.get(i).getIdFuncionario()),
                    func.get(i).getNome(),
                    func.get(i).getSenha(),
                    func.get(i).getTelefone(),
                    func.get(i).getCpf(),
                    func.get(i).getCargo(),
                    func.get(i).getSexo(),
                    String.valueOf(func.get(i).getSalario())});
                
             }//fecha for
             jTableFun.setModel(dtm);
         } catch (Exception e) {
             JOptionPane.showMessageDialog(rootPane,"Erro!"+e.getMessage());
         }//fecha catch
     }//fecha preenchertabela
    
    
