    private void buscarLinhaSelecionada(){
        try {
            /* Buscando a linha que o usuário clicou */
            int linha = jTableFun.getSelectedRow();
            
            /* Testando se o usuário selecionou alguma linha. */
            if (linha != -1) {
                
                /* todos os dados da linha selecionada pelo usuário */
                String idfuncionario = (String) jTableFun.getValueAt(linha, 0);
                String nome = (String) jTableFun.getValueAt(linha, 1);
                String senha = (String) jTableFun.getValueAt(linha, 2);
                String telefone = (String) jTableFun.getValueAt(linha, 3);
                String cpf = (String) jTableFun.getValueAt(linha, 4);
                
                String cargo = (String) jTableFun.getValueAt(linha, 5);
                String sexo = (String) jTableFun.getValueAt(linha, 6);
                String salario = (String) jTableFun.getValueAt(linha, 7);
               
               
               
                
                JOptionPane.showMessageDialog(this,
                        "Dados do Funcionario selecionado: "
                        +"\ncódigo: "+idfuncionario
                        +"\nNome: "+nome
                        +"\nSenha: "+senha
                        +"\nTelefone: "+telefone
                        +"\nCPF: "+cpf
                        +"\nCargo: "+cargo
                        +"\nSexo: "+sexo
                        +"\nSalário: "+salario
                        
                );
            } else {
                JOptionPane.showMessageDialog(this,
                        "Você não selecionou nenhuma linha!");
            }//fecha else
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                    "Erro ao selecionar uma linha! " + e.getMessage());
        }//fecha catch
    }//fecha método
